public class AnimalImages {

    private String name;
    public String imageName;

    public AnimalImages(String name, String imageName) {
        this.name = name;
        this.imageName = imageName;
    }

    public String getName() {
        return name;
    }

    public String getImageName() {
        return imageName;
    }




}
